class Programacion():
    def __init__(self, idProgramacion, fecha, idDepartment, elaborado_por, fecha_creacion, estado, fecha_cierre, cerrado_por, fecha_reapertura, reabierto_por, motivo_reapertura):
        self.idProgramacion = idProgramacion
        self.fecha = fecha
        self.idDepartment = idDepartment
        self.elaborado_por = elaborado_por
        self.fecha_creacion = fecha_creacion
        self.estado = estado
        self.fecha_cierre = fecha_cierre
        self.cerrado_por = cerrado_por
        self.fecha_reapertura = fecha_reapertura
        self.reabierto_por = reabierto_por
        self.motivo_reapertura = motivo_reapertura